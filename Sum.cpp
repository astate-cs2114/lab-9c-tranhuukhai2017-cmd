#include <iostream>
using namespace std;

int main() {
    int num1, num2, sum;

    // Ask user for input
    cout << "Enter first integer: ";
    cin >> num1;

    cout << "Enter second integer: ";
    cin >> num2;

    // Calculate sum
    sum = num1 + num2;

    // Output result
    cout << "The sum is: " << sum << endl;

    return 0;
}